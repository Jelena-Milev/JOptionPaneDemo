    private void jBtnConfirm5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirm5ActionPerformed
        ImageIcon icon = new ImageIcon("src/resources/java.png");
        int answer = JOptionPane.showConfirmDialog(this, "Poruka confirm dijaloga 5", "Naslov confirm dijaloga 5", JOptionPane.OK_CANCEL_OPTION, -1, icon);
        String answerString = switchUserAnswerOK(answer);
        jLblMessage5.setText(answer+": "+answerString); 
    }//GEN-LAST:event_jBtnConfirm5ActionPerformed

//    Definisane su 2 posebne metode iz razloga sto konstante YES i OK imaju istu vrednost, 0 
//    (jer se nikad ne nalaze zajedno u predefinisianim kombinacijama dugmica koje korisnik moze izabrati).
    private String switchUserAnswerYESNO(int answer) {
        if (answer == JOptionPane.YES_OPTION) {
            return "Yes";
        }
        if (answer == JOptionPane.NO_OPTION) {
            return "No";
        }
        if (answer == JOptionPane.CANCEL_OPTION) {
            return "Cancel";
        }
        if (answer == JOptionPane.CLOSED_OPTION) {
            return "Closed";
        }
        return "";
    }
    
    private String switchUserAnswerOK(int answer) {
        if (answer == JOptionPane.OK_OPTION) {
            return "OK";
        }
        if (answer == JOptionPane.CANCEL_OPTION) {
            return "Cancel";
        }
        if (answer == JOptionPane.CLOSED_OPTION) {
            return "Closed";
        }
        return "";
    }

