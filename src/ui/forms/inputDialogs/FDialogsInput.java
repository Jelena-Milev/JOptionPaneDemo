    private void jBtnOption6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOption6ActionPerformed
//     Sesti overload metode showInputDialog omoguÄava nam da podesimo sve: roditeljsku formu dijaloga,
//     poruku na dijalogu, nalov dijaloga i vrstu poruke, ikonicu po zelji umesto predefinisane, 
//     tip komponente pomocu koje korisnik unosi ulaz kao i podrazumevanu vrednost ulaza koji korisnik unosi 
//     showInputDialog(Component parentComponent, Object message, String title, int messageType, Icon icon, Object[] selectionValues, Object initialSelectionValue)
        ImageIcon icon = new ImageIcon("src/resources/java.png");

//      Ova metoda nam omogucava da putem formalnog parametra selectionValues prosledimo listu opcija za korisnika koje ce se prikazati u JComboBox-u odakle treba da izabere jednu opciju
//      Opcije treba da prosledimo u nizu jer je tip selectionValues parametra Object[], niz objekata klase Object, nadklase svih klasa u Javi, te zbog toga mozemo da prosledimo
//      niz objekata bilo koje klase
//      Pomocu parametra initialSelectionValue prosledjujemo referencu objekta iz niza selectionValues koji zelimo da bude prikazan kao podrazumevani izbor (taj objekat je 
//      selektovan kada se prikaze dijalog)
        String[] choices = {"Prvi izbor", "drugi izbor", "treci izbor"};
        Object input = JOptionPane.showInputDialog(this, "Poruka input dijaloga 6.1", "Nalov input dijaloga 6.1", -1, icon, choices, null);
        
//      Napomena: ova provera je dodata jer je povratna vrednost ovog overload-a metode objekat klase Object (za razliku od ostalih overload-ova koji vracaju String) 
//      i vraca null ukoliko korisnik nije odabrao neku opciju iz opadajuce liste vec je samo zatvorio dijalog (promenljiva input bi imala vrednost null)
        if (input != null) {
            jLblMessage6.setText(input.toString());
        }
//        

    }//GEN-LAST:event_jBtnOption6ActionPerformed

    private void jBtnOption7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnOption7ActionPerformed

        Fruit apple = new Fruit("apple", "green");
        Fruit pear = new Fruit("pear", "yellow");
        Fruit plum = new Fruit("plum", "purple");
        Fruit[] choices = new Fruit[]{apple, pear, plum};

//      Kao selectionValues parametar prosledjujemo niz objekata klase Fruit, oni ce biti prikazani u JComboBox-u tako sto ce za svaki biti pozvana toString metoda
//      Kao initialSelectionValue prosledjujemo referencu plum (koja je u nizu koji prosledjujemo kao selectionValues) i zbog toga ce po prikazivanju dijaloga
//      selektovana opcija biti objekat cija se referenca cuva u promenljivoj plum
        Object input = JOptionPane.showInputDialog(this, "Poruka input dijaloga 6.2", "Nalov input dijaloga 6.2", JOptionPane.QUESTION_MESSAGE, null, choices, plum);

        if (input != null) {
            Fruit usersMessage = (Fruit) input;
            jLblMessage7.setText(usersMessage.toString());
        }
    }//GEN-LAST:event_jBtnOption7ActionPerformed

